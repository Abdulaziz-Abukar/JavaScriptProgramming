/*
Get Date Components

Create a Date object for the current date. Log the year, month, day, hours, minutes, and seconds separately to the console.
*/

let theDate = new Date();

console.log(theDate.getFullYear());
console.log(theDate.getMonth() + 1);
console.log(theDate.getDate());
