/* Comparison and Logical Operators

Create variables x and y with values 10 and 20. Use a combination of comparison and logical operators to check if x is less than y and both are greater than 5. 
Log the result to the console. */

// Create variables x and y with values 10 and 20
var x = 10;
var y = 20;

// Check if x is less than y and both are greater than 5
if (x < y && x > 5 && y > 5) {
    console.log("x is less than y and both are greater than 5.");
} else {
    console.log("Condition not met.");
}
