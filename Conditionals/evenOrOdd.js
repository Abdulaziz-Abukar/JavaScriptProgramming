/* Even or Odd

Create a variable number and assign it any integer value. 
Use an if else statement to check if the number is even or odd. Log "The number is even" or "The number is odd" to the console accordingly. */


var num = 52;

if (num % 2 === 0) {
    console.log(`the number ${num} is even`);
}
else {
    console.log(`the number ${num} is odd`);
}
